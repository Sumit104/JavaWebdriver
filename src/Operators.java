
public class Operators {

	public static void main(String[] args) {
		// Arithmatic Operators
		
		int a=10;
		int b=20;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a*b);
		System.out.println(a%b);
		
		System.out.println('\n');
		
		//Relational operators
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
		
		
		

	}

}
